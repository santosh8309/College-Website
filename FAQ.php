        <h2>FAQ</h2>
        
            <p style="text-align: justify" >
           &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Indian Institute of Information Technology Vadodara is an institute established by the Government of India under Public Private Partnership (PPP) mode.  The partners  in this project  are Government of India, Government of Gujarat,  Tata Consultancy Services, Gujarat Energy Research and Management Institute and Gujarat State Fertilizers and Chemicals.  The Institute was established in the year  2013.  The first batch that  was admitted in the year 2013-14 is in its 3rd year now.  The Institute is presently functioning from Block Number 9 of Government Engineering College Gandhinagar. The second batch was admitted in the academic year 2014-15.   Both the batches put together have 168 students.  The Institute does not have its own hostel building,  therefore, a private premises has been hired to house  the hostel of the institute.  Students are transported by bus to the Institute.  The mess facility is available at hostel.</p></br>

<p>
<b>Q. While the name of the Institute is Indian Institute of Information Technology Vadodara, why it is     located at Gandhinagar and not at Vadodara? </b><br>

<b>A.</b> The Institute will be allotted land in Vadodara by the Government of Gujarat. Thereafter the institute activities will be moved to Vadodara.  Till such time the institute will keep functioning from the temporary location in Gandhinagar. <br></p></br>	


<p><b>Q. How do I get admission in IIIT Vadodara? </b><br>

<b>A.</b> Admissions are through  the Joint Seat Allocation Authority 2015 (JoSAA - <a href="http://josaa.nic.in" target="_blank"> www.http://josaa.nic.in</a>) constituted by the Government of India.  The Institute does not conduct its own entrance examination.  The admissions are purely based on All India Ranking (AIR) announced by the Central Board for Secondary Education (CBSE). There is no quota for Home State or for NRI.  The only reservation available is for Scheduled Castes, Scheduled Tribes, Other Backward Classes featuring  in the list of Government of India and for the PH category. <u>Latest Advertisement CSAB - 2015 <a href="PDF/27-06-2015 CSAB Press Release_to_PI.pdf" target="_blank"> Click here </a>.</u></p></br>


<p><b>Q. What is the facility of hostels? </b><br>

<b>A.</b> Since the institute does not have any permanent infrastructure in Gandhinagar,  a private premises is rented for the purpose of housing the hostel facility.  The facility has rooms which can accommodate 4 students per room.  The hostel is about 4 kms away from the Institue.   Buses, on regular basis run between the Hostel and the Institute.  It is proposed to have the mess facility in the hostel premises and in near future tender for operating the mess will be floated.</p></br>

<p><b>Q. What is the role of the Mentor Institute? </b><br>

<b>A.</b> The role of mentoring institute is mainly to ensure that the academic activity of IIITV continues efficiently.  Institute is having few faculty members on its own rolls.  Apart from these regular faculty the mentoring institute that is DAIICT faculty members teach some courses as visiting faculty.</p></br>                                                        

<p><b>Q. What about Placements? </b><br>

<b>A.</b> The institute is in its  3rd year and it does not have any placement records as of now.  It is expected that the private partners will help the Institute in placement efforts.</p></br>

